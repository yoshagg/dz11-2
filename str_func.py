def megaupper(arg:str) -> str:
    ###
    # :param str arg: string-type simbols needs to uppercase
    # :returns str with all simvols uppercased
    ###

    return arg.upper()

def megatitle(arg:str) -> str:

    ###
    # :param str arg: string-type simbols needs to uppercase
    # :returns str with all titled words
    ###

    return arg.title()

